/**
 * Definition of all the Mac Os shortcuts in a more readable way
 */
#define M_CP       LG(C)              // copy
#define M_PASTE    LG(V)              // paste
#define M_LOCK     LG(LC(Q))          // lock screen
#define M_SAVE     LG(S)              // save
#define M_RAYCAST  LG(SPC)            // raycast
#define M_UNDO     LG(Z)              // undo
#define M_CUT      LG(X)              // cut
#define FINDER     LG(E)              // finder
#define M_S_ALL    LG(A)              // select all
#define HYPER      LG(LC(LA(LSHIFT))) // press all 4 modifiers -> raycast hyper key