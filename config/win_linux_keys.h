/**
 * Definition of all the Windows/Linux keyboard shortcuts in a more readable way
 */

#define W_CP       LC(C)   // copy
#define W_PASTE    LC(V)   // paste
#define W_LOCK     LG(L)   // lock screen
#define W_SAVE     LC(S)   // save
#define W_RAYCAST  LA(SPC) // raycast
#define W_UNDO     LC(Z)   // undo
#define W_CUT      LC(X)   // undo
#define W_EXPL     LG(E)   // exploarer
#define W_S_ALL    LC(A)   // select all